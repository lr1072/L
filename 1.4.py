# 第1行修正：使用花括号创建字典
данные = {"положительные": 0, "отрицательные": 0, "ноль": 0}

# 检查文件是否存在，或使用更简单的路径
try:
    with open("sequence.txt", 'r', encoding='utf-8') as файл:
        for строка in файл:
            число = float(строка.strip())
            if число > 0:
                данные["положительные"] += 1
            elif число < 0:
                данные["отрицательные"] += 1
            else:
                данные["ноль"] += 1
except FileNotFoundError:
    print("文件未找到！请确保 sequence.txt 文件存在")
    exit()

# 修正第12-13行
total = sum(данные.values())
проценты = {категория: (значение / total) * 100 for категория, значение in данные.items()}

# 修正第15行和第17行：变量名不能有空格
высота_диаграммы = 10

for i in range(высота_диаграммы, 0, -1):
    строка = ""
    for категория in ["положительные", "отрицательные", "ноль"]:
        процент = проценты[категория]
        if (процент / 100) * высота_диаграммы >= i:
            строка += "█ "
        else:
            строка += "  "
    print(строка)

# 添加图例
print("正数    负数    零")
print(f"正数: {данные['положительные']} ({проценты['положительные']:.1f}%)")
print(f"负数: {данные['отрицательные']} ({проценты['отрицательные']:.1f}%)")
print(f"零: {данные['ноль']} ({проценты['ноль']:.1f}%)")